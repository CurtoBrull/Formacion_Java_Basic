public class Main {
	public static void main(String[] args) {

		// Enteros

		byte number = 1;
		short number2 = 11;
		int number3 = 111;
		long number4 = 111111;

		System.out.println("Numeros enteros: " + number + " , " + number2 + " , " + number3 + " , " + number4 + "\n");

		// Decimales

		float decimal = 1.1f;
		double decimal2 = 9.99d;

		System.out.println("Decimales: " + decimal + " , " + decimal2 + "\n");

		// Strings

		String nombre = "Javier";
		String apellido = "Curto";

		// Character
		char caracter = 'a';

		System.out.println("Strings: " + nombre + " " + apellido + "\n");
		System.out.println("Caracter: " + caracter + "\n");

		// Booleanos

		boolean verdadero = true;
		boolean falso = false;
	}
}